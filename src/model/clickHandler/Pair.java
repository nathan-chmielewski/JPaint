package model.clickHandler;

// Pair class to hold points for mouse click and drag for Draw, Move, Select commands

public class Pair {
    private int x;
    private int y;

    public Pair(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
